DB_NAME_RE = re.compile(r"^[A-Za-z0-9_]+$")
PUBLIC_DIR_RE = re.compile(r"^[A-Za-z0-9._/-]*$")
MYSQL_SERVICE_RE = re.compile(r"^mysql[0-9]+$")
JOB_RE = re.compile(r"^[A-Za-z0-9_.-]+$")


class StackError(RuntimeError):
    pass


def now() -> str:
    return _dt.datetime.now(_dt.timezone.utc).replace(microsecond=0).isoformat()


def rel(path: Path | str) -> str:
    p = Path(path)
    try:
        return str(p.resolve().relative_to(ROOT))
    except Exception:
        return str(p)


def info(message: str) -> None:
    print(message)


def warn(message: str) -> None:
    print(f"Warning: {message}", file=sys.stderr)


def die(message: str) -> None:
    raise StackError(message)


def validate(value: str, pattern: re.Pattern[str], label: str) -> str:
    if not value or not pattern.match(value):
        die(f"Invalid {label}: {value}")
    return value


def parse_env_file(path: Path) -> dict[str, str]:
    env: dict[str, str] = {}
    if not path.exists():
        return env
    for raw in path.read_text().splitlines():
        line = raw.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        key, value = line.split("=", 1)
        key = key.strip()
        value = value.strip().strip('"').strip("'")
        if key:
            env[key] = value
    return env


def stack_env() -> dict[str, str]:
    env = parse_env_file(ROOT / ".env")
    merged = dict(env)
    merged.update(os.environ)
    return merged


def default_php_version() -> str:
    return stack_env().get("DEFAULT_PHP_VERSION", "8.4")


def default_mysql_service() -> str:
    return stack_env().get("DEFAULT_MYSQL_SERVICE", "mysql84")


def mysql_root_password(env: dict[str, str], service: str) -> str | None:
    return env.get(f"{service.upper()}_ROOT_PASSWORD") or env.get("MYSQL_ROOT_PASSWORD")


def php_service_for(version: str) -> str:
    return "php" + version.replace(".", "")


def php_cron_service_for(version: str) -> str:
    return php_service_for(version) + "-cron"


def php_cli_service_for(version: str) -> str:
    return php_service_for(version) + "-cli"


def php_version_config_dir(version: str) -> Path:
    return PHP_VERSIONS_DIR / version


def cron_dir_for(version: str) -> Path:
    return CRON_RUNTIME_DIR / php_service_for(version)


def cron_jobs_dir_for(version: str) -> Path:
    return cron_dir_for(version) / "jobs"


def empty_db() -> dict[str, Any]:
    return {
        "schema": SCHEMA_VERSION,
        "defaults": {
            "php_version": default_php_version(),
            "mysql_service": default_mysql_service(),
        },
        "apps": {},
        "domains": {},
        "sites": {},
        "users": {},
        "crons": {},
        "updated_at": now(),
    }


def state_path() -> Path:
    if DB_PATH.exists():
        return DB_PATH
    if LEGACY_DB_PATH.exists():
        warn(f"using legacy state at {rel(LEGACY_DB_PATH)}; run './manage.py state migrate' to move it to {rel(DB_PATH)}")
        return LEGACY_DB_PATH
    return DB_PATH


def normalize_db(data: dict[str, Any]) -> dict[str, Any]:
    schema = int(data.get("schema", 0) or 0)
    if schema > SCHEMA_VERSION:
        die(f"state schema is {schema}; this CLI expects schema {SCHEMA_VERSION}")
    data["schema"] = SCHEMA_VERSION
    data.setdefault("defaults", {})
    data["defaults"].setdefault("php_version", default_php_version())
    data["defaults"].setdefault("mysql_service", default_mysql_service())
    data.setdefault("apps", {})
    data.setdefault("domains", {})
    data.setdefault("sites", {})
    data.setdefault("users", {})
    data.setdefault("crons", {})
    for app_name, app in data.get("apps", {}).items():
        if isinstance(app, dict):
            app.setdefault("name", app_name)
            if app.get("vhost"):
                app["vhost"] = rel(app_vhost_path(app_name))
    for domain, site in data.get("sites", {}).items():
        if isinstance(site, dict) and site.get("type") == "proxy":
            site.setdefault("domain", domain)
            site["vhost"] = rel(NGINX_VHOST_DIR / f"{domain}.conf")
    return data


def load_db() -> dict[str, Any]:
    path = state_path()
    if not path.exists():
        return empty_db()
    try:
        data = json.loads(path.read_text())
    except json.JSONDecodeError as exc:
        die(f"Cannot parse {rel(path)}: {exc}")
    if not isinstance(data, dict):
        die(f"{rel(path)} must contain a JSON object")
    return normalize_db(data)


def save_db(data: dict[str, Any]) -> None:
    normalize_db(data)
    data["schema"] = SCHEMA_VERSION
    data["updated_at"] = now()
    text = json.dumps(data, indent=2, sort_keys=True) + "\n"
    mkdir(DB_PATH.parent)
    fd, tmp_name = tempfile.mkstemp(prefix=".stack.", suffix=".json", dir=str(DB_PATH.parent))
    try:
        with os.fdopen(fd, "w") as fh:
            fh.write(text)
        os.replace(tmp_name, DB_PATH)
    finally:
        if os.path.exists(tmp_name):
            os.unlink(tmp_name)


def upsert_timestamp(item: dict[str, Any]) -> None:
    item.setdefault("created_at", now())
    item["updated_at"] = now()


def mkdir(path: Path, mode: int | None = None) -> None:
    path.mkdir(parents=True, exist_ok=True)
    if mode is not None:
        try:
            path.chmod(mode)
        except PermissionError:
            warn(f"could not chmod {rel(path)}")


def write_text(path: Path, content: str, mode: int | None = None) -> None:
    mkdir(path.parent)
    path.write_text(content)
    if mode is not None:
        try:
            path.chmod(mode)
        except PermissionError:
            warn(f"could not chmod {rel(path)}")


def render_template_text(text: str, values: dict[str, Any]) -> str:
    for key, value in values.items():
        text = text.replace(f"__{key}__", str(value))
    return text


def template_text(path: Path, values: dict[str, Any]) -> str:
    if not path.exists():
        die(f"Missing template: vibeops/{rel(path)}")
    return render_template_text(path.read_text(), values)


def write_template(path: Path, template: Path, values: dict[str, Any], mode: int | None = None, *, generated: bool = False) -> None:
    content = template_text(template, values)
    if generated:
        content = GENERATED_HEADER + content
    write_text(path, content, mode)


def mysql_string_literal(value: str) -> str:
    return value.replace("\\", "\\\\").replace("'", "''")


def command_exists(name: str) -> bool:
    return shutil.which(name) is not None


def run(cmd: list[str], *, input_text: str | None = None, check: bool = True, capture: bool = False) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        cmd,
        cwd=str(ROOT),
        input=input_text,
        text=True,
        check=check,
        stdout=subprocess.PIPE if capture else None,
        stderr=subprocess.PIPE if capture else None,
    )


def docker_available() -> bool:
    return command_exists("docker")


def running_services() -> set[str]:
    if not docker_available():
        return set()
    cp = run(
        ["docker", "compose", "ps", "--services", "--filter", "status=running"],
        check=False,
        capture=True,
    )
    if cp.returncode != 0:
        return set()
    return {line.strip() for line in cp.stdout.splitlines() if line.strip()}


def service_running(service: str) -> bool:
    return service in running_services()


def nginx_reload(no_reload: bool = False) -> None:
    if no_reload:
        return
    if service_running("nginx"):
        run(["docker", "compose", "exec", "-T", "nginx", "nginx", "-t"])
        run(["docker", "compose", "exec", "-T", "nginx", "nginx", "-s", "reload"])
        info("Reloaded nginx")
    else:
        info("nginx container is not running; start it then run: docker compose exec nginx nginx -t && docker compose exec nginx nginx -s reload")


def php_disable_default_pool(service: str) -> None:
    # Official PHP images ship [www] fragments in more than one file (for
    # example www.conf and sometimes docker.conf). VibeOps uses only generated
    # per-user pools, so disable any default [www] fragments at runtime too.
    run([
        "docker", "compose", "exec", "-T", service, "sh", "-lc",
        "for f in /usr/local/etc/php-fpm.d/*.conf; do "
        "[ -e \"$f\" ] || continue; "
        "if grep -q '^\\[www\\]' \"$f\"; then mv \"$f\" \"$f.disabled\"; fi; "
        "done; "
        "rm -f /usr/local/etc/php-fpm.d/zz-pools.conf; "
        "printf '[global]\\nerror_log = /proc/self/fd/2\\nprocess.max = 32\\ninclude=/usr/local/etc/php-fpm.d/pools/*.conf\\n' > /usr/local/etc/php-fpm.d/zz-vibeops.conf; ",
    ], check=False)


def php_reload(service: str, username: str, no_reload: bool = False) -> None:
    if service_running(service):
        php_disable_default_pool(service)
        run(["docker", "compose", "exec", "-T", service, "php-user-sync", username])
        if not no_reload:
            run(["docker", "compose", "exec", "-T", service, "php-fpm", "-tt"])
            run([
                "docker", "compose", "exec", "-T", service, "sh", "-lc",
                "kill -USR2 1",
            ])
            info(f"Reloaded {service}")
    else:
        info(f"{service} is not running; run/restart it to create the Linux user inside that PHP container.")


def rebuild_supercronic_crontab(php_version: str) -> Path:
    cron_dir = cron_dir_for(php_version)
    mkdir(cron_dir)
    combined = cron_dir / ".supercronic.cron"
    lines = [
        "SHELL=/bin/sh",
        "PATH=/usr/local/sbin:/usr/local/bin:/usr/sbin:/usr/bin:/sbin:/bin",
        "",
    ]
    for cron_file in sorted(cron_jobs_dir_for(php_version).glob("*.cron")):
        lines.append(f"# /usr/local/etc/php/cron.d/{cron_file.name}")
        lines.append(cron_file.read_text().rstrip("\n"))
        lines.append("")
    write_text(combined, "\n".join(lines) + "\n", 0o644)
    return combined


def cron_reload(service: str, usernames: Iterable[str] = ()) -> None:
    if service_running(service):
        script = r'''
set -eu
php-user-sync "$@"
cmdline="$(tr '\000' ' ' < /proc/1/cmdline || true)"
case "$cmdline" in
  *supercronic*) kill -USR2 1 ;;
  *) echo "Supercronic is not running as PID 1 ($cmdline)" >&2; exit 42 ;;
esac
'''
        cp = run(["docker", "compose", "exec", "-T", service, "sh", "-lc", script, "--", *usernames], check=False)
        if cp.returncode == 0:
            info(f"Reloaded {service} cron with SIGUSR2")
        elif cp.returncode == 42:
            run(["docker", "compose", "restart", service])
            info(f"Restarted idle {service} to start Supercronic")
        else:
            die(f"Failed to reload {service} cron (exit {cp.returncode})")
    else:
        info(f"{service} is not running; start it to load this cron job.")


def read_uid_from_env(path: Path) -> int | None:
    try:
        for line in path.read_text().splitlines():
            if line.startswith("UID="):
                return int(line.split("=", 1)[1])
    except Exception:
        return None
    return None


def allocate_uid(app_name: str, explicit_uid: int | None, db: dict[str, Any]) -> int:
    if explicit_uid is not None:
        return explicit_uid

    existing = db.get("apps", {}).get(app_name, {}).get("uid")
    if isinstance(existing, int):
        return existing

    for base in (PHP_VERSIONS_DIR, LEGACY_PHP_VERSIONS_DIR):
        for path in sorted(base.glob(f"*/users.d/{app_name}.env")):
            uid = read_uid_from_env(path)
            if uid is not None:
                return uid

    max_uid = 0
    for app in db.get("apps", {}).values():
        uid = app.get("uid") if isinstance(app, dict) else None
        if isinstance(uid, int):
            max_uid = max(max_uid, uid)
    for base in (PHP_VERSIONS_DIR, LEGACY_PHP_VERSIONS_DIR):
        for path in base.glob("*/users.d/*.env"):
            uid = read_uid_from_env(path)
            if uid is not None:
                max_uid = max(max_uid, uid)
    return 10000 if max_uid < 10000 else max_uid + 1


def generate_password(length: int = 20) -> str:
    alphabet = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789"
    return "".join(secrets.choice(alphabet) for _ in range(length))


def create_mysql_user(username: str, password: str | None, mysql_service: str) -> tuple[bool, str | None]:
    env = stack_env()
    mysql_service = validate(mysql_service, MYSQL_SERVICE_RE, "MySQL service")
    if not (ROOT / ".env").exists() or not service_running(mysql_service):
        info(f"Skipped MySQL user creation; start {mysql_service} and rerun, or pass --no-mysql.")
        return False, None
    root_password = mysql_root_password(env, mysql_service)
    if not root_password:
        info(f"Skipped MySQL user creation; {mysql_service.upper()}_ROOT_PASSWORD or MYSQL_ROOT_PASSWORD is missing.")
        return False, None

    cred_dir = HOME_DIR / username / ".credentials"
    cred_path = cred_dir / f"{mysql_service}.env"
    existing_password = parse_env_file(cred_path).get("MYSQL_PASSWORD") if cred_path.exists() else None
    password = password or env.get(f"{mysql_service.upper()}_USER_PASSWORD") or env.get("MYSQL_USER_PASSWORD") or existing_password or generate_password()
    mkdir(cred_dir, 0o700)
    write_template(cred_path, MYSQL_TEMPLATE_DIR / "user-credentials.env.template", {
        "USERNAME": username,
        "MYSQL_SERVICE": mysql_service,
        "MYSQL_PASSWORD": password,
    }, 0o600)

    sql = template_text(MYSQL_TEMPLATE_DIR / "create-user.sql.template", {
        "USERNAME": username,
        "MYSQL_PASSWORD_SQL": mysql_string_literal(password),
    })
    run(["docker", "compose", "exec", "-T", mysql_service, "mysql", "-uroot", f"-p{root_password}"], input_text=sql)
    info(f"MySQL account on {mysql_service}: {username} / {password}")
    info(f"Saved: vibeops/{rel(cred_path)}")
    return True, rel(cred_path)



def app_home(app_name: str) -> Path:
    return HOME_DIR / app_name


def app_www(app_name: str) -> Path:
    return app_home(app_name) / DOCROOT_NAME


def validate_public_dir(public_dir: str | None) -> str:
    public_dir = (public_dir or "").strip().strip("/")
    if public_dir in {".", ".."} or public_dir.startswith("../") or "/../" in public_dir or public_dir.endswith("/.."):
        die(f"Invalid public_dir: {public_dir}")
    if not PUBLIC_DIR_RE.match(public_dir):
        die(f"Invalid public_dir: {public_dir}")
    return public_dir


def app_document_root(app_name: str, public_dir: str | None = "") -> Path:
    public_dir = validate_public_dir(public_dir)
    return app_www(app_name) / public_dir if public_dir else app_www(app_name)


def container_document_root(app_name: str, public_dir: str | None = "") -> str:
    public_dir = validate_public_dir(public_dir)
    base = f"/home/{app_name}/{DOCROOT_NAME}"
    return f"{base}/{public_dir}" if public_dir else base


def app_vhost_path(app_name: str) -> Path:
    return NGINX_VHOST_DIR / f"app-{app_name}.conf"


def render_template(template: Path, destination: Path, values: dict[str, Any]) -> None:
    write_template(destination, template, values, generated=True)


def normalize_aliases(alias: Iterable[str] | None, aliases: str | None) -> list[str]:
    out: list[str] = []
    for value in alias or []:
        for item in value.split(","):
            item = item.strip()
            if item:
                out.append(validate(item, DOMAIN_RE, "alias domain"))
    if aliases:
        for item in aliases.split(","):
            item = item.strip()
            if item:
                out.append(validate(item, DOMAIN_RE, "alias domain"))
    return sorted(dict.fromkeys(out))


def domains_for(main_domain: str, aliases: Iterable[str]) -> list[str]:
    ordered = [main_domain, *aliases]
    return list(dict.fromkeys(ordered))


def assert_domain_free(domain: str, db: dict[str, Any], *, allow_app: str | None = None, allow_domain: str | None = None) -> None:
    owner = db.get("domains", {}).get(domain)
    if not owner:
        return
    if owner.get("kind") == "php" and allow_app and owner.get("app") == allow_app:
        return
    if owner.get("kind") == "proxy" and allow_domain and owner.get("domain") == allow_domain:
        return
    die(f"Domain already exists in stack.json: {domain} ({owner.get('kind')})")


def apply_app_mysql_metadata(app: dict[str, Any], app_name: str, mysql_service: str, credential_path: str | None = None) -> None:
    app["mysql_service"] = mysql_service
    app["mysql_host"] = mysql_service
    app["mysql_port"] = 3306
    app["mysql_user_name"] = app_name
    if credential_path:
        app["mysql_credentials"] = credential_path


def create_database(app_name: str, suffix: str, mysql_service: str) -> str | None:
    mysql_service = validate(mysql_service, MYSQL_SERVICE_RE, "MySQL service")
    validate(suffix, DB_NAME_RE, "database suffix")
    db_full_name = f"{app_name}_{suffix}"
    env = stack_env()
    root_password = mysql_root_password(env, mysql_service)
    if (ROOT / ".env").exists() and service_running(mysql_service) and root_password:
        sql = template_text(MYSQL_TEMPLATE_DIR / "create-database.sql.template", {
            "DB_FULL_NAME": db_full_name,
            "USERNAME": app_name,
        })
        run(["docker", "compose", "exec", "-T", mysql_service, "mysql", "-uroot", f"-p{root_password}"], input_text=sql)
        info(f"MySQL database on {mysql_service}: {db_full_name}")
    else:
        info(f"Skipped database creation; {mysql_service} is not running, .env is missing, or root password is unset.")
    return db_full_name


def ensure_app_identity(app_name: str, php_version: str, db: dict[str, Any], *, uid: int | None = None, no_mysql: bool = False, mysql_password: str | None = None, mysql_service: str | None = None, no_reload: bool = False) -> dict[str, Any]:
    app_name = validate(app_name, APP_NAME_RE, "app_name")
    php_version = validate(php_version, PHP_VERSION_RE, "PHP version")
    mysql_service = validate(mysql_service or default_mysql_service(), MYSQL_SERVICE_RE, "MySQL service")
    app_uid = allocate_uid(app_name, uid, db)
    php_service = php_service_for(php_version)
    socket_group_name = stack_env().get("SOCKET_GROUP_NAME", "nginxsock")

    mkdir(app_home(app_name) / "logs", 0o770)
    mkdir(app_www(app_name))
    mkdir(app_home(app_name) / ".credentials", 0o700)
    mkdir(app_home(app_name) / ".composer")
    mkdir(app_home(app_name) / ".ssh", 0o700)
    mkdir(php_version_config_dir(php_version) / "users.d")
    mkdir(php_version_config_dir(php_version) / "pool.d")
    mkdir(cron_dir_for(php_version))
    mkdir(PHP_SOCKET_DIR / php_service)
    mkdir(PHP_LOG_DIR / php_service)

    fallback_path = php_version_config_dir(php_version) / "pool.d" / "zz-fallback.conf"
    if not fallback_path.exists():
        write_template(fallback_path, PHP_TEMPLATE_DIR / "fallback.conf.template", {"SOCKET_GROUP_NAME": socket_group_name}, generated=True)

    write_template(php_version_config_dir(php_version) / "users.d" / f"{app_name}.env", PHP_TEMPLATE_DIR / "user.env.template", {
        "USERNAME": app_name,
        "UID": app_uid,
    }, generated=True)
    write_template(php_version_config_dir(php_version) / "pool.d" / f"{app_name}.conf", PHP_TEMPLATE_DIR / "pool.conf.template", {
        "USERNAME": app_name,
        "SOCKET_GROUP_NAME": socket_group_name,
        "PHP_VERSION": php_version,
    }, generated=True)
    try:
        app_home(app_name).chmod(0o750)
    except PermissionError:
        warn(f"could not chmod home/{app_name}")

    info(f"Created PHP {php_version} app identity: {app_name} uid={app_uid}")
    info(f"Home: vibeops/{rel(app_home(app_name))}")
    info(f"Pool: vibeops/{rel(php_version_config_dir(php_version) / 'pool.d' / f'{app_name}.conf')}")
    info(f"Socket: vibeops/{rel(PHP_SOCKET_DIR / php_service / f'{app_name}.sock')}")

    php_reload(php_service, app_name, no_reload=no_reload)

    mysql_created = False
    credential_path = None
    if not no_mysql:
        mysql_created, credential_path = create_mysql_user(app_name, mysql_password, mysql_service)

    app = db["apps"].setdefault(app_name, {"name": app_name})
    app["uid"] = app_uid
    app["home"] = rel(app_home(app_name))
    public_dir = validate_public_dir(app.get("public_dir", ""))
    app["public_dir"] = public_dir
    app["root"] = rel(app_document_root(app_name, public_dir))
    app["php_version"] = php_version
    app["php_service"] = php_service
    versions = set(app.get("php_versions", []))
    versions.add(php_version)
    app["php_versions"] = sorted(versions)
    app.setdefault("databases", [])
    app.setdefault("tls", {"mode": "self-signed"})
    apply_app_mysql_metadata(app, app_name, mysql_service, credential_path)
    if mysql_created:
        app["mysql_user"] = True
    upsert_timestamp(app)
    return app


def tls_replacement(tls: dict[str, Any] | None) -> str:
    tls = tls or {"mode": "self-signed"}
    mode = tls.get("mode", "self-signed")
    if mode == "acme":
        return template_text(NGINX_TEMPLATE_DIR / "tls-acme.conf.template", {})
    if mode == "files":
        return template_text(NGINX_TEMPLATE_DIR / "tls-files.conf.template", {
            "CERT_PATH": tls.get("cert", ""),
            "CERT_KEY_PATH": tls.get("key", ""),
        })
    return template_text(NGINX_TEMPLATE_DIR / "tls-self-signed.conf.template", {})


def apply_vhost_tls(conf_path: Path, record: dict[str, Any]) -> None:
    tls = record.get("tls") if isinstance(record.get("tls"), dict) else {"mode": "self-signed"}
    replace_tls_block(conf_path, tls_replacement(tls))
    set_https_redirect(conf_path, bool(tls.get("redirect_https")) and tls.get("mode") in {"acme", "files"}, quiet=True)


