import shutil
import subprocess
import sys
import tempfile
from pathlib import Path
from typing import Any, Iterable

ROOT = Path(__file__).resolve().parents[1]
CONFIG_DIR = ROOT / "config"
RUNTIME_DIR = ROOT / "runtime"
STATE_DIR = RUNTIME_DIR / "state"
DB_PATH = STATE_DIR / "stack.json"
LEGACY_DB_PATH = ROOT / "stack.json"
GENERATED_DIR = RUNTIME_DIR / "generated"
CUSTOM_DIR = RUNTIME_DIR / "custom"
HOME_DIR = RUNTIME_DIR / "home"
PHP_CONFIG_DIR = CONFIG_DIR / "php"
PHP_GENERATED_DIR = GENERATED_DIR / "php"
PHP_VERSIONS_DIR = PHP_GENERATED_DIR / "versions"
LEGACY_PHP_VERSIONS_DIR = PHP_CONFIG_DIR / "versions"
PHP_TEMPLATE_DIR = PHP_CONFIG_DIR / "templates"
MYSQL_TEMPLATE_DIR = CONFIG_DIR / "mysql" / "templates"
NGINX_TEMPLATE_DIR = CONFIG_DIR / "nginx" / "templates"
NGINX_VHOST_DIR = GENERATED_DIR / "nginx" / "vhosts"
CERTS_DIR = RUNTIME_DIR / "certs"
PHP_SOCKET_DIR = RUNTIME_DIR / "run" / "php-fpm"
PHP_LOG_DIR = RUNTIME_DIR / "logs" / "php"
CRON_RUNTIME_DIR = GENERATED_DIR / "cron"
SCHEMA_VERSION = 3
DOCROOT_NAME = "www"
