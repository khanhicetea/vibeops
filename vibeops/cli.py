#!/usr/bin/env python3
"""Pure-Python management CLI for VibeOps.

VibeOps is a vibe-coding ops stack. No third-party dependencies are required.
The CLI keeps lightweight metadata in runtime/state/stack.json while generating
disposable runtime config consumed by Docker, PHP-FPM, supercronic, and nginx.
"""
